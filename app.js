let currentLanguage = localStorage.getItem('gcoffee-language') || 'ro';
let currentSlide = 0;
let carouselPaused = false;
let carouselTimer;

const reduceMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
const header = document.querySelector('.site-header');
const menuToggle = document.querySelector('.menu-toggle');
const mobileMenu = document.querySelector('.mobile-menu');
const backToTop = document.querySelector('.back-to-top');
const bookingDialog = document.querySelector('#booking-dialog');

function applyLanguage(language) {
  const copy = translations[language] || translations.ro;
  currentLanguage = language;
  document.documentElement.lang = language;
  document.title = language === 'ro' ? 'G-Coffee Luxury Showroom | Chișinău' : 'G-Coffee Luxury Showroom | Кишинёв';
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const value = copy[element.dataset.i18n];
    if (value) element.textContent = value;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const value = copy[element.dataset.i18nPlaceholder];
    if (value) element.placeholder = value;
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((element) => {
    const key = element.dataset.i18nAria;
    element.setAttribute('aria-label', copy[key] || '');
  });
  document.querySelectorAll('[data-lang]').forEach((button) => {
    const active = button.dataset.lang === language;
    button.classList.toggle('is-active', active);
    button.setAttribute('aria-pressed', String(active));
  });
  localStorage.setItem('gcoffee-language', language);
}

function closeMenu() {
  menuToggle.setAttribute('aria-expanded', 'false');
  mobileMenu.hidden = true;
  document.body.classList.remove('menu-open');
}

function showSlide(index) {
  const slides = [...document.querySelectorAll('.hero__slide')];
  const dots = [...document.querySelectorAll('.hero__dot')];
  currentSlide = (index + slides.length) % slides.length;
  slides.forEach((slide, slideIndex) => {
    const active = slideIndex === currentSlide;
    slide.classList.toggle('is-active', active);
    slide.setAttribute('aria-hidden', String(!active));
  });
  dots.forEach((dot, dotIndex) => {
    const active = dotIndex === currentSlide;
    dot.classList.toggle('is-active', active);
    dot.setAttribute('aria-selected', String(active));
  });
}

function restartCarousel() {
  window.clearInterval(carouselTimer);
  if (!carouselPaused && !reduceMotion && !document.hidden) {
    carouselTimer = window.setInterval(() => showSlide(currentSlide + 1), 7000);
  }
}

function setCarouselPaused(paused) {
  carouselPaused = paused;
  const button = document.querySelector('.hero__play');
  const copy = translations[currentLanguage];
  button.classList.toggle('is-paused', paused);
  button.setAttribute('aria-pressed', String(paused));
  button.setAttribute('aria-label', paused ? copy.playCarousel : copy.pauseCarousel);
  button.querySelector('span').textContent = paused ? copy.playCarousel : copy.pauseCarousel;
  button.innerHTML = paused
    ? '<svg viewBox="0 0 24 24" aria-hidden="true"><path d="M8 5v14l11-7L8 5Z" /></svg><span class="sr-only"></span>'
    : '<svg viewBox="0 0 24 24" aria-hidden="true"><path d="M7 5h4v14H7V5Zm6 0h4v14h-4V5Z" /></svg><span class="sr-only"></span>';
  button.querySelector('span').textContent = paused ? copy.playCarousel : copy.pauseCarousel;
  restartCarousel();
}

function openBooking(product = '') {
  if (product) {
    const select = bookingDialog.querySelector('select');
    const option = [...select.options].find((item) => item.value === product);
    if (option) select.value = product;
  }
  bookingDialog.showModal();
  bookingDialog.querySelector('input').focus();
}

function validateForm(form) {
  const copy = translations[currentLanguage];
  let valid = true;
  const requiredFields = [...form.querySelectorAll('input[required]:not([type="checkbox"])')];
  requiredFields.forEach((field) => {
    const error = form.querySelector(`#${field.id}-error`);
    let message = '';
    if (!field.value.trim()) message = copy.requiredError;
    if (field.type === 'tel' && field.value.trim() && !/^[+\d][\d\s()\-]{7,}$/.test(field.value.trim())) message = copy.phoneError;
    field.setAttribute('aria-invalid', String(Boolean(message)));
    if (error) error.textContent = message;
    if (message) valid = false;
  });
  const privacy = form.querySelector('input[name="privacy"]');
  if (privacy) {
    const privacyError = form.querySelector('#privacy-error');
    const message = privacy.checked ? '' : copy.privacyError;
    privacy.setAttribute('aria-invalid', String(Boolean(message)));
    privacyError.textContent = message;
    if (message) valid = false;
  }
  return valid;
}

function handleDemoSubmit(event) {
  event.preventDefault();
  const form = event.currentTarget;
  const status = form.querySelector('.form-status');
  status.textContent = '';
  if (!validateForm(form)) {
    form.querySelector('[aria-invalid="true"]')?.focus();
    return;
  }
  status.textContent = translations[currentLanguage].success;
  form.reset();
}

document.querySelectorAll('[data-lang]').forEach((button) => {
  button.addEventListener('click', () => applyLanguage(button.dataset.lang));
});

menuToggle.addEventListener('click', () => {
  const open = menuToggle.getAttribute('aria-expanded') === 'true';
  menuToggle.setAttribute('aria-expanded', String(!open));
  mobileMenu.hidden = open;
  document.body.classList.toggle('menu-open', !open);
});

mobileMenu.querySelectorAll('a').forEach((link) => link.addEventListener('click', closeMenu));

document.querySelectorAll('[data-open-booking]').forEach((button) => {
  button.addEventListener('click', () => {
    closeMenu();
    openBooking(button.dataset.product || '');
  });
});

document.querySelector('[data-close-booking]').addEventListener('click', () => bookingDialog.close());
bookingDialog.addEventListener('click', (event) => {
  const rect = bookingDialog.getBoundingClientRect();
  const inside = event.clientX >= rect.left && event.clientX <= rect.right && event.clientY >= rect.top && event.clientY <= rect.bottom;
  if (!inside) bookingDialog.close();
});

document.querySelectorAll('[data-slide-to]').forEach((button) => {
  button.addEventListener('click', () => { showSlide(Number(button.dataset.slideTo)); restartCarousel(); });
});
document.querySelector('[data-hero-prev]').addEventListener('click', () => { showSlide(currentSlide - 1); restartCarousel(); });
document.querySelector('[data-hero-next]').addEventListener('click', () => { showSlide(currentSlide + 1); restartCarousel(); });
document.querySelector('.hero__play').addEventListener('click', () => setCarouselPaused(!carouselPaused));
document.querySelector('.hero').addEventListener('mouseenter', () => window.clearInterval(carouselTimer));
document.querySelector('.hero').addEventListener('mouseleave', restartCarousel);
document.addEventListener('visibilitychange', restartCarousel);

document.querySelectorAll('form').forEach((form) => form.addEventListener('submit', handleDemoSubmit));

window.addEventListener('scroll', () => {
  const scrolled = window.scrollY > 90;
  header.classList.toggle('is-sticky', scrolled);
  backToTop.classList.toggle('is-visible', window.scrollY > 700);
}, { passive: true });

backToTop.addEventListener('click', () => window.scrollTo({ top: 0, behavior: reduceMotion ? 'auto' : 'smooth' }));
window.addEventListener('resize', () => { if (window.innerWidth >= 1050) closeMenu(); });

applyLanguage(currentLanguage);
showSlide(0);
if (reduceMotion) setCarouselPaused(true);
else restartCarousel();
